L, N, W, H, i = 0, 0, 0, 0, 0
L = int(input())
N = int(input())
while i <= N-1:
    W, H = [int(n) for n in input().split(" ")]
    if W<L or H<L:
        print('UPLOAD ANOTHER')
    elif W == H:
        print('ACCEPTED')
    else:
        print('CROP IT')
    i = i + 1

